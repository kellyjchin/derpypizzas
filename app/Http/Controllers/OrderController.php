     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $newOrder = new Order();
        $newOrder->name = request('name');
        $newOrder->order_items = request('food-item');
        $newOrder->quant = request('quant');
        $newOrder->total = request('total');
        
        $newOrder->save();
        $confirmedOrderMessage = "Thanks for your order, {$request->name}! \r\nYour total is \${$request->total}.";
        
        // redirect to welcome page
        return redirect('/')->with('message', $confirmedOrderMessage);
    }

